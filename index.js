function toRoman(num) {
    let list = [
        [1000, 'M'],
        [900, 'CM'],
        [500, 'D'],
        [400, 'CD'],
        [100, 'C'],
        [90, 'XC'],
        [50, 'L'],
        [40, 'XL'],
        [10, 'X'],
        [9, 'IX'],
        [5, 'V'],
        [4, 'IV'],
        [1, 'I'],
    ]

    for(let i = 0; i < list.length; i++){
    
        if(num < 1){
            return ''
        }else if(num >= list[i][0]){
            return list[i][1] + toRoman(num - list[i][0])
        }  
    }
}


